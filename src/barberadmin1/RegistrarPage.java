    private boolean validarCampoUsuario() {
        Matcher matcher = USERNAME_PATTERN.matcher(jtxtNombre.getText());
        if (!matcher.matches()) {
            JOptionPane.showMessageDialog(null, "Porfavor intente usando otro usuario");
            return false;
        } else if (validInputs.validarExistUser(jtxtUsuario.getText())) {
            JOptionPane.showMessageDialog(null, "Usuario ya existe, Favor elegir uno distinto");
            return false;
        } else {
            return true;
        }
    }

